public class monthlyReportRecord {
    String name;
    Boolean isExpense;
    int amount;
    int mexpense;

    public monthlyReportRecord(String name, Boolean isExpense, int amount, int mexpense) {
        this.name = name;
        this.isExpense = isExpense;
        this.amount = amount;
        this.mexpense = mexpense;
    }

}